package chap03.lecture.binary;

public class AssignmentOperator {
	public static void main(String[] args) {
		// = += -= *= /= %=
		// &= |= ^=
		// <<= >>= >>>=
		
		int i = 3;
		i += 5;
		System.out.println(i);
		i *= 2;
		System.out.println(i);
		i <<= 1;
		System.out.println(i);
		
		
	}
}










